package org.idp.server;

import org.springframework.boot.SpringApplication;
import org.springframework.boot.autoconfigure.SpringBootApplication;
import org.springframework.context.annotation.Bean;
import org.springframework.data.redis.connection.RedisConnectionFactory;
import org.springframework.data.redis.connection.jedis.JedisConnectionFactory;
import org.springframework.data.redis.core.RedisOperations;
import org.springframework.data.redis.core.RedisTemplate;
import org.springframework.data.redis.serializer.GenericJackson2JsonRedisSerializer;
import org.springframework.data.redis.serializer.StringRedisSerializer;

@SpringBootApplication
public class IdPApplication {

  public static void main(String[] args) {
    SpringApplication.run(IdPApplication.class, args);
  }

  @Bean
  public RedisConnectionFactory redisConnectionFactory() {
    // 必要に応じてhost, port変更してな
    return new JedisConnectionFactory();
  }

  @Bean
  public RedisOperations<String, Object> redisOperations(RedisConnectionFactory connectionFactory) {
    RedisTemplate<String, Object> template = new RedisTemplate<>();
    template.setConnectionFactory(connectionFactory);
    template.setKeySerializer(new StringRedisSerializer());
    template.setValueSerializer(new GenericJackson2JsonRedisSerializer());
    return template;
  }

  @Bean
  public SafeRedisSessionRepository sessionRepository(RedisOperations<String, Object> redisOperations) {
    return new SafeRedisSessionRepository(redisOperations);
  }
}
